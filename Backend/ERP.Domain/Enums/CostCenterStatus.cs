namespace ERP.Domain.Enums;

/// <summary>
/// يحدد قاعدة مركز التكلفة على مستوى الحساب في دليل الحسابات.
/// يُستخدم لفرض قواعد التحليل المالي عند ترحيل القيود.
/// </summary>
public enum CostCenterStatus
{
    /// <summary>
    /// مركز التكلفة إلزامي — لا يُقبل ترحيل أي قيد على هذا الحساب دون تحديد مركز تكلفة.
    /// </summary>
    Required = 1,

    /// <summary>
    /// مركز التكلفة اختياري — يمكن ترحيل القيد مع أو بدون مركز تكلفة.
    /// </summary>
    Optional = 2,

    /// <summary>
    /// مركز التكلفة معطّل — لا يُسمح بربط أي مركز تكلفة بهذا الحساب.
    /// </summary>
    Disabled = 3
}
